package Lamda;

public class Apple {

    private int wight;
    private Color color;

    public Apple(int wight, Color color) {
        this.wight = wight;
        this.color = color;
    }




    public int getWight() {
        return wight;
    }

    public Color getColor() {
        return color;
    }

    public void setWight(int wight) {
        this.wight = wight;
    }

    public void setColor(Color color) {
        this.color = color;
    }

}
